/**
 *
 * @param {年柱} nz :甲子
 * @param {农历月} lunarM   ：正月0 腊月11 闰月按润哪个月处理 eg：润二月 取二月 1
 * @param {农历日} lunarD   ：初一0 三十29
 * @param {时令} sl         ： 时辰：地支
 * @eg getCGByW("辛丑",8,5,"子")
 * @returns [5.6,'五钱六两']
 */
const getCGByW = (nz = "癸卯", lunarM = 4, lunarD = 19, sl = "申") => {
    let nGan = nz.split("")[0]
    let nZhi = nz.split("")[1]
    let ganFD = Gan.indexOf(nGan)
    let zhiFD = Zhi.indexOf(nZhi)
    let arr = [ganFD]
    for (let i = 0; i < parseInt(101, 2); i++) {
        let offset = 1e1 - i * (1 << 1)
        let val = offset + ganFD > 1.1e1 ? offset + ganFD - 1.2e1 : offset + ganFD
        arr.push(val)
    }
    let dex = arr.indexOf(zhiFD)
    let w = W1[ganFD + (dex * 1e1)]
    w = numAdd(w, W2[lunarM])
    w = numAdd(w, W3[lunarD])
    w = numAdd(w, W4[Zhi.indexOf(sl)])
    let drr = (w + "").split('.')
    let cw = ''
    if (drr[1] != undefined) {
        cw = CNum[drr[0] - 1] + '两' + CNum[drr[1] - 1] + '钱'
    } else {
        cw = CNum[drr[0] - 1] + '两'
    }
    return [Number(w), cw]
}

/**
 *
 * @param {称骨钱数} weight
 * @param {性别} sex
 * @eg getCGByPoetry(2.1,1)
 * @tip 浮点数之间的计算会有精度溢出问题 所以必须都要转成个位的整数进行加减 question：5.1-2.1 = 2.99999999
 * @returns 短命非业谓大空，平生灾难事重重，凶祸频临陷逆境，终世困苦事不成
 */
const getCGByPoetry = (weight, sex) => {
    let dex = Math.trunc(weight - (1 << 1)) * 1e1
    let fex = 0
    let ws = "" + weight
    let drr = ws.split('.')
    if (drr[1] != undefined) {
        if (drr[1] - 1 < 0) {
            dex -= 10
            fex = drr[1] - 1 + 10
        } else {
            fex = drr[1] - 1
        }
    } else {
        fex = -1
    }
    let str = ''
    switch (sex) {
        case 0:
            str = P2.split(';')[dex + fex]
            break
        case 1:
            str = P1.split(';')[dex + fex]
            break
    }
    return str
}

/**
 * 加法运算，避免数据相加小数点后产生多位数和计算精度损失。
 *
 */
function numAdd(num1, num2) {
    var baseNum, baseNum1, baseNum2
    try {
        baseNum1 = num1.toString().split('.')[1].length
    } catch (e) {
        baseNum1 = 0
    }
    try {
        baseNum2 = num2.toString().split('.')[1].length
    } catch (e) {
        baseNum2 = 0
    }
    baseNum = Math.pow(10, Math.max(baseNum1, baseNum2))
    return (num1 * baseNum + num2 * baseNum) / baseNum
}
